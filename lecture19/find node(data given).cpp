void levelorder(node* root)
{
    if(root==NULL)
    {
        return;
    }
    queue<node*>q;
    q.push(root);
    while(!q.empty())
    {
        node*top=q.front();
        q.pop();
        cout<<top->data<<" ";
        if(top->left)
        {
            q.push(top->left);
        }
        if(top->right)
        {
            q.push(top->right);
        }
    }
    return;
}
bool findnode(node*root,int k)
{
    if(root==NULL)
    {
        return false;
    }
    if(root->data==k)
    {
        return true;
    }
    bool left=findnode(root->left,k);
    if(left)
    {
        return true;
    }
    else
    {
        return findnode(root->right,k);
    }
}
